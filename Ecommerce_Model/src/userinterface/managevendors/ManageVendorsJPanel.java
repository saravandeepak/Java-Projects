    private void searchVendorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchVendorButtonActionPerformed
        // TODO add your handling code here:
       if (searchText.getText().equals(""))
       {
          JOptionPane.showMessageDialog(this, "Please enter a Vendor Name to search", "Warning", JOptionPane.WARNING_MESSAGE); 
       }
       else{
           Vendor vendor = vendorCatalog.searchVendor(searchText.getText().toLowerCase());
           if(vendor == null){
                JOptionPane.showMessageDialog(this,"Vendor Name entered does not exist","Information", JOptionPane.INFORMATION_MESSAGE);
            }
           else{
                ViewVendorJPanel panel = new ViewVendorJPanel(userProcessContainer, vendor);
                userProcessContainer.add("ViewSupplierPanel", panel);
                CardLayout card = (CardLayout)userProcessContainer.getLayout();
                card.next(userProcessContainer);
            }
       }
    }//GEN-LAST:event_searchVendorButtonActionPerformed


