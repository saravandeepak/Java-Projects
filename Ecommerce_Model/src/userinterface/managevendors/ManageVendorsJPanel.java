    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)vendorListTable.getModel();
        dtm.setRowCount(0);
        for (Vendor vendor: vendorCatalog.getVendorCatalog()){
            Object[] row = new Object[2];
            row[0] = vendor;
            row[1] = vendor.getVendorEmail();
            dtm.addRow(row);
        }
    }
