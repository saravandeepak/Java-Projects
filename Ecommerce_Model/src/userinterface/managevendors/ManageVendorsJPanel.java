    private void viewVendorButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewVendorButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = vendorListTable.getSelectedRow();
        if(selectedRow >= 0)
        {
            Vendor vendor = (Vendor)vendorListTable.getValueAt(selectedRow,0);
            ViewVendorJPanel panel = new ViewVendorJPanel(userProcessContainer, vendor);
            userProcessContainer.add("ViewSupplierPanel", panel);
            CardLayout card = (CardLayout)userProcessContainer.getLayout();
            card.next(userProcessContainer);
                    
        }
        else 
        {
            JOptionPane.showMessageDialog(this, "Please select a row");
        
        }
        
        
    }//GEN-LAST:event_viewVendorButtonActionPerformed

